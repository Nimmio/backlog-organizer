import type { Metadata } from "next";
import "../globals.css";
import { SidebarInset, SidebarProvider } from "@/components/ui/sidebar";
import AppSidebar, {
  ISidebarLinks,
} from "@/components/layout/sidebar/app-sidebar";
import { Home, User } from "lucide-react";
import AppHeader from "@/components/layout/header/app-header";
import AppMain from "@/components/layout/main/app-main";
import { BreadcrumStoreProvider } from "@/providers/breadcrum-store-provider";
import { UserStoreProvider } from "@/providers/user-store-provider";
import { auth } from "@/auth";
import { headers } from "next/headers";
import { redirect } from "next/navigation";
import { ThemeProvider } from "@/providers/theme-provider";
import { getCurrentUserSettings } from "@/lib/user";
import ThemeSetter from "@/components/themeSetter/ThemeSetter";
export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const sidebarLinks: ISidebarLinks = {
    groups: [{ elements: [{ label: "Home", link: "/", icon: Home }] }],
  };

  const sessionResponse = await auth.api.getSession({
    headers: await headers(),
  });

  const user = sessionResponse?.user || null;
  if (!user)
    auth.api
      .signOut({
        headers: await headers(),
      })
      .then(redirect("/sign-in"));

  const theme = (await getCurrentUserSettings())?.theme || "SYSTEM";

  return (
    <html lang="en" suppressHydrationWarning>
      <body>
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <UserStoreProvider user={user}>
            <ThemeSetter />
            <BreadcrumStoreProvider>
              <SidebarProvider>
                <AppSidebar links={sidebarLinks} />
                <SidebarInset>
                  <AppHeader withSidebarTrigger />
                  <main>
                    <AppMain>{children}</AppMain>
                  </main>
                </SidebarInset>
              </SidebarProvider>
            </BreadcrumStoreProvider>
          </UserStoreProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
